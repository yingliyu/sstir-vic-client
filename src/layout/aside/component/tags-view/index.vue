<template>
  <div class="tags-view-container">
    <scroll-pane ref="scrollPane" class="tags-view-wrapper">
      <router-link
        v-for="tag in Array.from(visitedViews)"
        ref="tag"
        :class="isActive(tag)?'active':''"
        :to="tag"
        :key="tag.path"
        class="tags-view-item"
        @contextmenu.prevent.native="openMenu(tag,$event)"
      >
        {{ $t(tag.title) }}
        <span class="el-icon-close" @click.prevent.stop="closeSelectedTag(tag)" />
      </router-link>
    </scroll-pane>
    <ul v-show="visible" :style="{left:left+'px',top:top+'px'}" class="contextmenu">
      <li @click="refreshSelectedTag(selectedTag)">{{$t('layout.tagView.refresh')}}</li>
      <li @click="closeSelectedTag(selectedTag)">{{$t('layout.tagView.close')}}</li>
      <li @click="closeOthersTags">{{$t('layout.tagView.closeOther')}}</li>
      <li @click="closeAllTags">{{$t('layout.tagView.closeAll')}}</li>
    </ul>
  </div>
</template>

<script>
import ScrollPane from '@/components/scroll-pane'

import { mapGetters, mapActions } from 'vuex'

export default {
  name: 'TagsView',

  components: {
    ScrollPane
  },

  data () {
    return {
      visible: false,
      top: 0,
      left: 0,
      selectedTag: {}
    }
  },

  mounted () {
    this.addViewTags()
  },

  methods: {
    ...mapActions(['addView', 'delCachedView', 'delView', 'delOthersViews', 'delAllViews']),
    generateRoute () {
      if (this.$route.name) {
        return this.$route
      }
      return false
    },

    isActive (route) {
      return route.path === this.$route.path
    },

    addViewTags () {
      const route = this.generateRoute()
      if (!route) {
        return false
      }
      this.addView(route)
    },

    moveToCurrentTag () {
      const tags = this.$refs.tag
      this.$nextTick(() => {
        for (const tag of tags) {
          if (tag.to.path === this.$route.path) {
            this.$refs.scrollPane.moveToTarget(tag.$el)
            break
          }
        }
      })
    },

    refreshSelectedTag (view) {
      this.delCachedView(view).then(() => {
        const { fullPath } = view
        this.$nextTick(() => {
          this.$router.replace({
            path: '/redirect' + fullPath
          })
        })
      })
    },

    closeSelectedTag (view) {
      if (this.visitedViews.length === 1 && this.visitedViews[0].name === 'dashboard') {
        return false
      } else {
        this.delView(view).then(({ visitedViews }) => {
          if (this.isActive(view)) {
            const latestView = visitedViews.slice(-1)[0]
            if (latestView) {
              this.$router.push(latestView)
            } else {
              this.$router.push('/home/dashboard')
            }
          }
        })
      }
    },
    closeOthersTags () {
      this.$router.push(this.selectedTag)
      this.delOthersViews(this.selectedTag).then(() => {
        this.moveToCurrentTag()
      })
    },
    closeAllTags () {
      if (this.visitedViews.length === 1 && this.visitedViews[0].name === 'dashboard') {
        return false
      } else {
        this.delAllViews()
        this.$router.push('/')
      }
    },

    openMenu (tag, e) {
      this.visible = true
      this.selectedTag = tag
      const offsetLeft = this.$el.getBoundingClientRect().left // container margin left
      this.left = e.clientX - offsetLeft + 15 // 15: margin right
      this.top = e.clientY
    },

    closeMenu () {
      this.visible = false
    }
  },

  watch: {
    $route () {
      this.addViewTags()
      this.moveToCurrentTag()
    },

    visible (val) {
      if (val) {
        document.body.addEventListener('click', this.closeMenu)
      } else {
        document.body.removeEventListener('click', this.closeMenu)
      }
    }
  },

  computed: {
    ...mapGetters(['visitedViews'])
  }
}
</script>

